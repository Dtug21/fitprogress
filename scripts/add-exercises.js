let content = fs.readFileSync(filePath, 'utf8');

// Find the closing bracket of the array before the export
const insertBefore = '];\n\nexport const exercises';
const idx = content.indexOf(insertBefore);
if (idx === -1) {
  console.error('Could not find insertion point!');
  process.exit(1);
}

const newContent = content.slice(0, idx) + newExercises + content.slice(idx);
fs.writeFileSync(filePath, newContent, 'utf8');
console.log('Done! File updated successfully.');

// Count exercises
const idMatches = newContent.match(/id: '/g);

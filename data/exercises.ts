export function getExerciseById(id: string): Exercise | undefined {
  return exercises.find((e) => e.id === id);
}

export function getExercisesByMuscle(muscle: string): Exercise[] {
  return exercises.filter((e) => e.muscle_group === muscle);
}

export function getExercisesByMode(mode: 'home' | 'gym'): Exercise[] {
  return exercises.filter((e) => e.mode === mode || e.mode === 'both');
}

export function getExercisesByEquipment(available: string[]): Exercise[] {
  return exercises.filter((e) =>
    e.equipment_required.every((eq) => available.includes(eq))
  );
}
